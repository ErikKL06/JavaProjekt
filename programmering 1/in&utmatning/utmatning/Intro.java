package utmatning;

public class Intro {

	public static void main(String[] args) {
		//Utmatning
		System.out.print("Hej hej");
		System.out.print("Megga n....");
		
		//Utskrift med rad byte:
		System.out.println("hej");  //radbyte efter
		System.out.println("hejhej");
		
		// \ skapar olika formatteringsalternativ för text:
		System.out.println("Här börjar på rad ett \nHär kommer rad två"); // Alltså \n ger mig en ny rad
		System.out.println("För att tabba \tSå får jag luft i raden"); // \t gör en tabb mellan ord
		
		// Extra radbyte:
		System.out.println("");
		System.out.println("Chik");
		
		
		// För snyggare utskrifter kolla upp System.out.printf()
		
	}

}
